<template>
  <tr>
    <td>{{ articleno }}</td>
    <th class="text-left">
      <router-link :to="{ name: 'noticeview', params: { articleno: articleno } }">{{ subject }}</router-link>
    </th>
    <td>{{ hit }}</td>
    <td>{{ userid }}</td>
    <td>{{ regtime | dateFormat }}</td>
  </tr>
</template>

<script>
import moment from "moment";

export default {
  name: "NoticeListItem",
  props: {
    articleno: Number,
    userid: String,
    subject: String,
    hit: Number,
    regtime: String,
  },
  filters: {
    dateFormat(regtime) {
      return moment(new Date(regtime)).format("YY.MM.DD");
    },
  },
};
</script>

